package com.irandubamodulo01.model;

import java.util.ArrayList;
import java.util.List;

public class RelatorioHistoricoAlteracaoValor {
	
	private Long id;
	
	private String descricao;
	
	private String observacao;
	
	private Integer sequencia;
	
	public static List<RelatorioHistoricoAlteracaoValor> listaHistorico;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

	public String getObservacao() {
		return observacao;
	}

	public void setObservacao(String observacao) {
		this.observacao = observacao;
	}

	public static List<RelatorioHistoricoAlteracaoValor> getListaHistorico() {
		
		if (listaHistorico != null)
			return listaHistorico;
		else
			return new ArrayList<RelatorioHistoricoAlteracaoValor>();
	}

	public Integer getSequencia() {
		return sequencia;
	}

	public void setSequencia(Integer sequencia) {
		this.sequencia = sequencia;
	}
	
	

}
